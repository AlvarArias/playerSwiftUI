import Foundation
import SwiftUI

/**
An extension to the Color class that adds custom colors.

Usage: Access the custom colors as static properties on the Color class.

*/
extension Color {
    
    /// A custom primary color.
    static let newPrimaryColor = Color("Verde")
    /// A custom secondary color.
    static let newSecundaryColor = Color("Cafe 1")
    /// A custom tertiary color.
    static let newTerciaryColor = Color("Cafe 2")
    /// A custom light gray color.
    static let newColorGrayLight = Color("Gris light")
    /// A custom light green color.
    static let newColorGreenLight = Color("Verde light")
    }



